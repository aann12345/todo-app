/** Определяет категорию по названию товара; null — не покупка/неизвестно. */
export function categorize(title: string): string | null {
  const t = title.toLowerCase()
  for (const [category, keywords] of Object.entries(DICT)) {
    if (keywords.some((k) => t.includes(k))) return category
  }
  return null
}
